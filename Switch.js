let color = prompt("esciba una letra A, B o C")

switch (color) {

    case "A":
    console.log("A de azul");
    break;

    case  "B":
    console.log("B de Beige");
    break;

    case "C":
    console.log("C de Cian");
    break;

    default:
        console.log("No integrado");
        break;

}